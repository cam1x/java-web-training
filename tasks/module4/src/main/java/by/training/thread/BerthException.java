package by.training.thread;

public class BerthException extends Exception {
    public BerthException() {
        super();
    }
    public BerthException(String message, Throwable cause) {
        super(message, cause);
    }
    public BerthException(String message) {
        super(message);
    }
    public BerthException(Throwable cause) {
        super(cause);
    }
}