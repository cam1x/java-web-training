package by.training.command;

public class DOMParserCommandException extends CommandException {

    public DOMParserCommandException(String msg) {
        super(msg);
    }

    public DOMParserCommandException(String message, Throwable cause) {
        super(message, cause);
    }

    public DOMParserCommandException(Throwable cause) {
        super(cause);
    }
}
